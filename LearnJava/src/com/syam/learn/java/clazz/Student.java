package com.syam.learn.java.clazz;

public class Student {

	String stuName;
	String stuCollege;
	String stuDepartment;
	int completedYear;
	
	String studentId;

	public String getStuName() {
		return stuName;
	}

	public void setStuName(String stuName) {
		this.stuName = stuName;
	}

	public String getStuCollege() {
		return stuCollege;
	}

	public void setStuCollege(String stuCollege) {
		this.stuCollege = stuCollege;
	}

	public String getStuDepartment() {
		return stuDepartment;
	}

	public void setStuDepartment(String stuDepartment) {
		this.stuDepartment = stuDepartment;
	}

	public int getCompletedYear() {
		return completedYear;
	}

	public void setCompletedYear(int completedYear) {
		this.completedYear = completedYear;
	}

}
