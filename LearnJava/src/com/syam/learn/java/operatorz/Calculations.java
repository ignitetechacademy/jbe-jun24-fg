package com.syam.learn.java.operatorz;

public class Calculations {

	int add(int firstValue, int secondValue) {
		int total = firstValue + secondValue;
		return total;
	}
	
	void addition(int firstValue, int secondValue) {
		int total = firstValue + secondValue;

		System.out.println("Added value : " + total);
	}
	
	int sub(int firstValue, int secondValue) {
		int total = firstValue - secondValue;
		return total;
	}
	
	int mul(int firstValue, int secondValue) {
		int total = firstValue * secondValue;
		return total;
	}
	
	int div(int firstValue, int secondValue) {
		int total = firstValue / secondValue;
		return total;
	}
}
